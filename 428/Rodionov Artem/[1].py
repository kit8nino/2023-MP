def sylvester():
    a = 2
    yield a
    for i in range(9):
        a = a * a + 1
        yield a       
        
#–ß–∏—Å–ª–∞ —Ç—Ä–∏–±–æ–Ω–∞—á—á–∏

def tribonacci():
    a, b, c = 0, 0, 1
    yield a
    yield b
    yield c
    for i in range(7):
        d = a + b + c
        yield d
        a = b
        b = c
        c = d        
        
#–ß–∏—Å–ª–∞ –õ–µ–æ–Ω–∞—Ä–¥–æ

def leonardo():
    a, b = 1, 1
    yield a
    yield b
    for i in range(8):
        a, b = b, a + b + 1
        yield b
        
